package org.loopingstatement;
//create project ,class, main method
public class NestedForLoopConcept {
	public static void main(String[] args) {
//	type for + ctrl + space
//		click enter
//		similarly create for loop inside the for loop
		for (int i = 1; i <= 3; i++) {
			for (int j = 1; j <= 3; j++) {
				System.out.print(j+" ");
			}
			System.out.println();
			
		}
	}
	

}
